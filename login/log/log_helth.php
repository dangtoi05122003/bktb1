      <div class="rich-text"><p></p><p>Thuốc phòng bệnh sốt rét là một loại thuốc không thể thiếu với người chuẩn bị có kế hoạch đi du lịch. Theo đó, một số loại thuốc phòng bệnh sốt rét nên có như sau:</p><ul><li>Mefloquin: Là thuốc phòng sốt rét hay được dùng nhất tác dụng trên vùng kháng và nhạy cảm với chloroquin, dùng 1 lần mỗi tuần trước khi đến vùng có sốt rét 1-2 tuần. Thuốc có thể xảy ra các tác dụng phụ như chóng mặt, <a href=""><b>rối loạn tiêu hóa</b></a>, không sử dụng cho trẻ em nặng &lt; 5kg, phụ nữ có thai 3 tháng đầu, bệnh nhân có tiền sử tâm thần, động kinh, có bất thường dẫn truyền ở tim.</li><li>Proguanil (Paludrine): uống 2 viên 100mg hàng ngày, sử dụng được cho trẻ em &gt; 10 tuổi, phụ nữ mang thai hoặc nghi ngờ có thai.</li><li>Halofantrin và dẫn xuất artemisinin: Dùng phòng bệnh sốt rét kháng nhiều loại thuốc khác.</li></ul><p>Một số loại thuốc khác nên trang bị như sau: Doxycycline, cloroquin phosphat, Primaquin phosphat có thể lựa chọn khi có chống chỉ định các thuốc khác.</p></div>
  

  

  
      

      
  

  
      <h3>3.10. Bệnh tiêu chảy của người đi du lịch</h3>
  

  

  
      

      
  

  
      <div class="rich-text"><p></p><p>Bệnh tiêu chảy của người đi du lịch là một hội chứng đặc trưng bởi số lần đại tiện tăng lên gấp 2 lần, kèm theo đau quặn bụng, buồn nôn, trướng bụng, mót rặn, sốt, khó chịu. Các triệu chứng xảy ra đột ngột có thể trong chuyến đi hoặc sau khi về nhà và có thể tự khỏi. Tiêu phân lỏng có thể trên 5 lần 1 ngày, kéo dài có thể 3-4 ngày, đôi khi cả tuần, cá biệt có khi cả 1- &gt;3 tháng. Việc điều trị nhanh chóng có thể rút ngắn thời gian bị bệnh xuống còn vài giờ. Yếu tố gây bệnh thường gặp nhất là các loại vi khuẩn sinh độc tố ruột, <a href=""><b>Escherichia coli</b></a>, Shigella, Campylobacter jejuni, Salmonella và Vibrio. Các virus thường la <a href=""><b>rotavirus</b></a>, Norwalk virus. Các ký sinh trùng chủ yếu là Giardia lamblia và Entamoeba histolytica. Ngoài ra có đến 20-50% trường hợp tiêu chảy vẫn chưa lý giải được.</p><p>Cách phòng bệnh: An toàn thực phẩm là cách phòng bệnh, trong 1 số trường hợp đặc biệt (Sự kiện thể thao, cuộc họp quan trọng, tuần trăng mật) có thể sử dụng thuốc để phòng bệnh: Bismuth subsalicylate 2v X 4 lần / ngày, doxycylin 100mg mỗi ngày, trimethroprim/sulphamethoxazol 160/800mg mỗi ngày, norfloxacine 400mg mỗi ngày, ciproloxacine 500mg mỗi ngày.</p><p>Trong trường hợp mắc bệnh, nếu không bị sốt, không đi ngoài ra máu nên dùng 2 viên loperamid sau lần đi ngoài lỏng đầu tiên, sau đó dùng liều thông thường, nếu vẫn còn tiêu chảy đến lần thứ 3 uống 2 viên ciproloxacin hoặc norfloxacin, uống bù nước bằng dung dịch Oresol, không dùng<b> kháng sinh</b>, hoặc tác nhân chống tiêu chảy cho trẻ em, tạm dừng các thực phẩm có bơ sữa, nếu không thuyên giảm nên đến cơ sở y tế gần nhất để thăm khám.</p></div>
  

  

  
      

      
  

  
      <h3>3.11. Chú ý đến bệnh sợ độ cao</h3>
  

  

  
      

      
  

  
      <div class="rich-text"><p></p><p>Khi lên đến cao trên 3000m so với mực nước biển, hơn 80% du khách sẽ có một số triệu chứng của bệnh say núi cấp, càng lên cao nhanh càng xuất hiện triệu chứng của bệnh, bao gồm <a href=""><b>phù phổi</b></a> và <a href=""><b>phù não</b></a> do độ cao, vì áp suất riêng phần của oxy trong không khí giảm, gây thiếu oxy máu. Say núi cấp tính có thể tăng từ nhẹ đến nặng, nhẹ bao gồm các triệu chứng như đau đầu, buồn nôn mệt mỏi, nặng như nôn mửa, khó thở khi nghỉ ngơi, mất điều hòa, tâm thần yếu, hoặc tím tái. Phù phổi do độ cao có thể xuất hiện sau 1 vài ngày, thường là trong khi ngủ với các triệu chứng như khó thở nặng, ho, nhiều ran, sốt nhẹ và tím tái. Phù não do độ cao có thể xuất hiện sau vài ngày của say núi cấp tính nhẹ với các biểu hiện liên tục mệt mỏi, mất dần sự tỉnh táo, rối loạn tâm thần, dấu hiệu thần kinh khu trú và hôn mê. Không có cách dự phòng hoàn hảo, chỉ phòng ngừa bằng cách mỗi ngày lên cao &lt; 300m, đối với trường hợp leo cao hơn 3000m kéo dài trong hơn 12 giờ dự phòng bằng 250mg Acetazolamid (Diamox) mỗi ngày bắt đầu 24 giờ trước khi leo và tiếp tục trong 1 số ngày đầu ở độ cao. Có thể mang theo thuốc giảm đau đầu thông thường, acetazolamid có thể dùng trong trong trường hợp say núi nhẹ hoặc dùng dexamethason 4 mg mỗi 6 giờ để dự phòng, loại thuốc này được dành để điều trị các trường hợp bị các hội chứng độ cao nặng. Một khi dùng dexamethason, phải khẩn trương hạ độ cao xuống để có nồng độ oxy cao hơn.</p></div>
  

  

  
      

      
  

  
      <h3>3.12. Lưu ý đến một số bệnh lý nền có sẵn</h3>
  

  

  
      

      
  

  
      <div class="rich-text"><p></p><p>Thông thường người du lịch phải điều trị các bệnh sẵn sớm và tích cực. Chuẩn bị tinh thần và xử trí các tình huống tiến triển trầm trọng hơn các bệnh có sẵn.</p><p><b><i>Viêm phế quản/ khí phế thũng</i></b></p><p>Đối với người bệnh phổi tắc nghẽn mạn tính, khuyến cáo nên làm khí máu động mạch trước khi đi máy bay, vì máy bay thường bay độ cao 2500m, ở độ cao này ngưừi khỏe mạnh PaO2 giảm xuống còn khoảng 60mmHg, nên trang bị oxy cho người bệnh trước khi lên máy bay có PaO2 &lt; 70mmHg hoặc độ bảo hòa Oxy &lt; 93% trong suốt chuyến bay. Những người có tăng carbon dioxid máu hoặc dung tích sống &lt; 50% không nên đi máy bay mà đổi sang phương tiện di chuyển khác.</p><p><b><i>Bệnh hen</i></b></p><p>Các khu vực phát triển thường ô nhiễm không khí, nên dự phòng tình huống khởi phát cơn hen, bạn nên biết cách tự dùng thuốc kháng sinh và thuốc corticosteroid theo khả năng của mình.</p><p><b><i>Bệnh tim</i></b></p><p>Nên ngăn cấm người bị <a href=""><b>nhồi máu cơ tim</b></a> đi máy bay trong vòng 4-8 tuần. Người bị bệnh tim mạch phải có khả năng đi bộ được 100m và leo dốc được 12 bậc thang mới khuyến cáo có thể di chuyển bằng máy bay.</p><p><b><i>Loét dạ dày/ phẫu thuật dạ dày</i></b></p><p>Người bệnh tuyệt đối không di chuyển bằng máy bay trong vòng 10-14 ngày sau phẫu thuật ổ bụng, phẫu thuật nội soi có bơm CO2 nên đi máy bay sau 48 giờ để đảm bảo an toàn. Vì giảm HCl và thuốc chẹn thụ thể H2 làm <a href=""><b>giảm sức đề kháng</b></a> tự nhiên của dạ dày đối với bệnh tiêu chảy và bệnh tả khi du lịch, nên cân nhắc dùng các thuốc dự phòng hoặc điều trị tích cực và mở rộng việc dùng vắc - xin để phòng bệnh.</p><p><b><i>Người tàn tàn tật - Viêm khớp</i></b></p><p>Nên dự trù trước các điều kiện thuận lợi cho người cao tuổi và tàn tật khi đi máy bay hay phương tiện vận chuyển khác. Kiểm soát được các khả năng làm trầm trọng thêm của bệnh viêm khớp.</p></div>
  

  

  
      

      
  

  
